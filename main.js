var slideId = 0;
showAnimatedSlides();

function showAnimatedSlides() {
  var i;
  var Slides = document.getElementsByClassName("animatedSlide");
  for (i = 0; i < Slides.length; i++) {
    Slides[i].style.display = "none";
  }
  slideId++;
  if (slideId > Slides.length) {
    slideId = 1;
  }
  Slides[slideId - 1].style.display = "block";
  setTimeout(showAnimatedSlides, 2000); // Change image every seconds
}

